int ft_atoi(const char *str)
{
    int i;
    int m;
    int nb;

    i = 0;
    m = 1;
    nb = 0;
    while (str[i] <= 32)
        i++;
    if (str[i] == '-')
    {
        m = -1;
        i++;
    }
    else if (str[i] == '+')
        i++;
    while (str[i] && (str[i] >= '0' && str[i] <= '9'))
    {
        nb = nb * 10 + str[i] - '0';
        i++;
    }
    return (nb * m);
}