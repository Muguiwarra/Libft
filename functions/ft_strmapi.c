#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))	//why do we use the function pointer's while the return is just a charracter
{
	char			*ptr;
	unsigned int	i;

	i = 0;
	ptr = (char *) malloc((ft_strlen(s) + 1 ) * sizeof(char));
	if (!ptr)
		return (0);
	while (s[i])
	{
		ptr[i] = f(i, s[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}